//Crie um array vazio. Em seguida use o método **`push()`** para adicionar
//3 números ao array. Em seguida, use o método **`pop()`** para remover
//o último número do array e exibir o array resultante.

namespace ex03 {
    //Crie um array vazio.
    let lista:number[] = [];
    //Use o método **`push()`** para adicionar 3 números ao array.
    lista.push(3, 5, 14);
    //Use o método **`pop()`** para remover o último número do array
    lista.pop();
    //Exibir o array resultante.
    console.log(lista);
}